x = 5
print(type(x))
# int

a = "Debarghya"
print(type(a))
# String

b = 5.0
print(type(b))
# float

c = 1j
print(type(c))
# complex

d = ["Debarghya", "Paramita"]
print(type(d))
# list

e = ("Debarghya", "Paramita")
print(type(e))
# tuple

f = range(6)
print(type(f))
# range

g = {"Name": "Debarghya", "Age": "27"}
print(type(g))
# dict

h = {"Debarghya", "Paramita"}
print(type(h))
# set

i = frozenset({"Debarghya", "Baj"})
print(type(i))
# Frozenset
